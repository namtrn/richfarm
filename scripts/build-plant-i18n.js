#!/usr/bin/env node

const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const sourceDir = path.join(rootDir, "packages", "convex", "convex", "data", "plantI18nSource");
const outputDir = path.join(rootDir, "packages", "convex", "convex", "data", "plantI18n");
const locales = ["en", "vi", "es", "fr", "pt", "zh"];

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function writeFile(filePath, content) {
  fs.writeFileSync(filePath, content);
}

function formatTsModule(constName, rows) {
  return [
    "import type { PlantI18nLocaleRow } from './types';",
    "",
    "// This file is generated by scripts/build-plant-i18n.js",
    "// Edit packages/convex/convex/data/plantI18nSource/*.json instead.",
    `export const ${constName}: PlantI18nLocaleRow[] = ${JSON.stringify(rows, null, 2)};`,
    "",
  ].join("\n");
}

function constantNameForLocale(locale) {
  const suffix = locale.charAt(0).toUpperCase() + locale.slice(1);
  return `plantI18n${suffix}SeedData`;
}

function main() {
  for (const locale of locales) {
    const jsonPath = path.join(sourceDir, `${locale}.json`);
    const tsPath = path.join(outputDir, `${locale}.ts`);
    const rows = readJson(jsonPath);

    if (!Array.isArray(rows)) {
      throw new Error(`${jsonPath} must contain an array`);
    }

    writeFile(tsPath, formatTsModule(constantNameForLocale(locale), rows));
    console.log(`built ${path.relative(rootDir, tsPath)} (${rows.length} rows)`);
  }
}

main();
